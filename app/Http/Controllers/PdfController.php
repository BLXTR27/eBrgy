    {
        $post = Resident::find($id);
        $cman = Officer::where('position','Chairman')->first();
        $sec = Officer::where('position','Secretary')->first();
        $pdf = DomPDF::loadView('Forms.BarangayClearance',compact('post','cman','sec'));
        $pdf->SetPaper('letter','portrait');;
        return $pdf->stream();
    }

